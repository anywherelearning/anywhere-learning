#!/usr/bin/env node
/**
 * Walks every blob in Vercel Blob, matches each filename to a product slug
 * (fuzzy substring), and emits lib/activity-blob-urls.ts with the mapping.
 *
 * Usage:  npx dotenv-cli -e .env.local -- node scripts/build-blob-url-map.mjs
 */

import { list } from '@vercel/blob';
import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

if (!process.env.BLOB_READ_WRITE_TOKEN) {
  console.error('BLOB_READ_WRITE_TOKEN required');
  process.exit(1);
}

const ROOT = process.cwd();

// Load every product slug from fallback-products.ts
const FB = readFileSync(join(ROOT, 'lib', 'fallback-products.ts'), 'utf-8');
const allSlugs = [...new Set([...FB.matchAll(/slug:\s*['"]([a-z0-9-]+)['"]/g)].map((m) => m[1]))];

// Plus the two synthesized Skills Map entries
const SPECIAL_SLUGS = ['skills-map-color', 'skills-map-bw'];
const slugs = [...allSlugs, ...SPECIAL_SLUGS];

function looseKey(s) {
  return s
    .toLowerCase()
    .replace(/[&,'.]/g, '')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

// Manual aliases for filenames that don't share enough tokens with the slug to
// match via the fuzzy matcher (e.g. seasonal packs use "Anywhere Learning"
// prefix in the filename which doesn't appear in the slug).
const MANUAL_FILENAME_TO_SLUG = [
  [/skills.map.*color|color.*skills.map/i, 'skills-map-color'],
  [/skills.map.*b.?w|b.?w.*skills.map/i, 'skills-map-bw'],
  [/anywhere learning spring/i, 'spring-outdoor-pack'],
  [/anywhere learning summer/i, 'summer-outdoor-pack'],
  [/anywhere learning fall/i, 'fall-outdoor-pack'],
  [/anywhere learning winter/i, 'winter-outdoor-pack'],
  [/nature walk task cards/i, 'nature-walk-task-cards'],
  [/nature choice boards/i, 'nature-choice-boards'],
  [/outdoor learning missions/i, 'outdoor-learning-missions'],
  [/outdoor stem challenge/i, 'outdoor-stem-challenges'],
  [/land art challenge cards/i, 'land-art-challenges'],
  [/nature crafts for kids/i, 'nature-crafts'],
  [/my nature journal/i, 'nature-journal-walks'],
];

function matchSlugForFilename(filename) {
  // Strip extension + path
  const stem = filename.replace(/\.[^.]+$/, '').replace(/^.*\//, '');

  // Skip the previews — these are samples, not full activities
  if (/^preview/i.test(stem)) return null;
  // Skip the free guide PDFs — those are lead magnets, not activities
  if (/^free guide/i.test(stem)) return null;

  // Manual aliases first
  for (const [re, slug] of MANUAL_FILENAME_TO_SLUG) {
    if (re.test(stem)) return slug;
  }

  const key = looseKey(stem);

  // 1) slug appears as substring
  for (const slug of slugs) {
    const sk = looseKey(slug);
    if (key.includes(sk)) return slug;
  }
  // 2) every word of the slug appears in the filename
  for (const slug of slugs) {
    const words = looseKey(slug).split(' ').filter((w) => w.length > 2);
    if (words.length >= 2 && words.every((w) => key.includes(w))) return slug;
  }
  return null;
}

async function main() {
  console.log('Fetching blobs…');
  const all = [];
  let cursor;
  do {
    const r = await list({ token: process.env.BLOB_READ_WRITE_TOKEN, cursor, limit: 1000 });
    all.push(...r.blobs);
    cursor = r.cursor;
  } while (cursor);
  console.log(`Got ${all.length} blobs.\n`);

  // Filter to PDFs only
  const pdfs = all.filter((b) => /\.pdf$/i.test(b.pathname));
  console.log(`PDFs: ${pdfs.length}`);

  const map = {};
  const unmatched = [];
  for (const b of pdfs) {
    const slug = matchSlugForFilename(b.pathname);
    if (!slug) {
      unmatched.push(b.pathname);
      continue;
    }
    // If multiple blobs match same slug (newer upload), prefer the most recent
    if (!map[slug] || new Date(b.uploadedAt) > new Date(map[slug].uploadedAt)) {
      map[slug] = { url: b.url, pathname: b.pathname, uploadedAt: b.uploadedAt };
    }
  }

  console.log(`\nMatched ${Object.keys(map).length} slugs.`);
  console.log(`Unmatched PDFs: ${unmatched.length}`);
  for (const u of unmatched.slice(0, 10)) console.log(`  ? ${u}`);
  if (unmatched.length > 10) console.log(`  …+${unmatched.length - 10} more`);

  // Slugs with no match
  const noBlob = slugs.filter((s) => !map[s]);
  console.log(`\nSlugs with no PDF: ${noBlob.length}`);
  for (const s of noBlob.slice(0, 10)) console.log(`  ? ${s}`);

  // Emit lib/activity-blob-urls.ts
  const out = [
    '/**',
    ' * AUTO-GENERATED by scripts/build-blob-url-map.mjs.',
    ' *',
    ' * Maps each product slug (and the two synthesized Skills Map entries) to',
    ' * the public Vercel Blob URL of its PDF. Used by the membership download',
    ' * endpoint to serve activity files after auth + tier checks.',
    ' *',
    ' * Re-run the script whenever PDFs are added/replaced in the Blob store.',
    ' */',
    '',
    'export const ACTIVITY_BLOB_URLS: Record<string, string> = {',
    ...Object.entries(map)
      .sort(([a], [b]) => a.localeCompare(b))
      .map(([slug, { url }]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(url)},`),
    '};',
    '',
    'export function getActivityBlobUrl(slug: string): string | null {',
    '  return ACTIVITY_BLOB_URLS[slug] || null;',
    '}',
    '',
  ].join('\n');
  writeFileSync(join(ROOT, 'lib', 'activity-blob-urls.ts'), out);
  console.log('\nWrote lib/activity-blob-urls.ts');
}

main().catch((err) => {
  console.error('Failed:', err);
  process.exit(1);
});
