#!/usr/bin/env node
/**
 * Reads scripts/skills-taxonomy.json and emits lib/skills.ts — the canonical
 * runtime taxonomy used by the catalogue filter and product pages.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const ROOT = process.cwd();
const TAXONOMY = JSON.parse(readFileSync(join(ROOT, 'scripts', 'skills-taxonomy.json'), 'utf-8'));

const FAMILIES_ORDERED = [
  'Critical Thinking & Reasoning',
  'Communication & Writing',
  'Planning & Self-Management',
  'Empathy & People',
  'Math & Money',
  'Creativity & Making',
  'Nature & Science',
  'Entrepreneurship & Money',
  'Tech & Digital',
  'World & Geography',
];

function toSlug(name) {
  return name
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

const familyEntries = FAMILIES_ORDERED.map((name) => ({
  slug: toSlug(name),
  name,
  count: TAXONOMY.familyCounts[name] || 0,
}));

const perSlugEntries = Object.entries(TAXONOMY.perSlug)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([slug, { canonical, families }]) => ({
    slug,
    canonical,
    familySlugs: families.map(toSlug),
  }));

const ts = `/**
 * AUTO-GENERATED by scripts/generate-skills-lib.mjs. Do not edit by hand.
 *
 * The 10 top-level Skill Families (used in the /shop filter) and the canonical
 * skill names per product slug (extracted by OCR from the actual activity PDFs
 * via scripts/extract-pdf-skills.mjs and reconciled by build-skills-taxonomy.mjs).
 */

export interface SkillFamily {
  /** URL-safe slug, e.g. "critical-thinking-and-reasoning". */
  slug: string;
  /** Human-facing label, e.g. "Critical Thinking & Reasoning". */
  name: string;
  /** How many activities are tagged with this family (for UI display). */
  count: number;
}

export const SKILL_FAMILIES: readonly SkillFamily[] = [
${familyEntries
  .map(
    (f) =>
      `  { slug: ${JSON.stringify(f.slug)}, name: ${JSON.stringify(f.name)}, count: ${f.count} },`,
  )
  .join('\n')}
];

export interface ProductSkills {
  /** Canonical skill names (display on the product page). */
  canonical: string[];
  /** Skill family slugs (used by the filter). */
  families: string[];
}

/**
 * Map of product slug → extracted skill data. Activities not in this map
 * (card packs, bundles, parent guides) keep their existing skillTags from
 * \`lib/product-descriptions.ts\` and are not filterable by family.
 */
export const productSkills: Record<string, ProductSkills> = {
${perSlugEntries
  .map(
    (e) =>
      `  ${JSON.stringify(e.slug)}: { canonical: ${JSON.stringify(e.canonical)}, families: ${JSON.stringify(e.familySlugs)} },`,
  )
  .join('\n')}
};

/** Look up skills for a product slug. Returns null if the activity wasn't
 *  extracted (e.g. it's a card pack or bundle). */
export function getProductSkills(slug: string): ProductSkills | null {
  return productSkills[slug] || null;
}

/** Find a family by slug. */
export function getFamily(slug: string): SkillFamily | undefined {
  return SKILL_FAMILIES.find((f) => f.slug === slug);
}
`;

writeFileSync(join(ROOT, 'lib', 'skills.ts'), ts);
console.log(`Wrote lib/skills.ts (${familyEntries.length} families, ${perSlugEntries.length} products)`);
